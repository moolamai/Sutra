/**
 * Soft caps (NFR — bounded stage / tool-call lists; no unbounded turn scans).
 */
export const TRAJECTORY_STAGE_LIMIT = 32;
export const TRAJECTORY_TOOL_CALL_LIMIT = 64;
export const TRAJECTORY_ID_LIMIT = 128;
export const TRAJECTORY_HASH_LIMIT = 128;
export const TRAJECTORY_MODEL_ID_LIMIT = 256;

export const TRAJECTORY_LOCALITIES = Object.freeze([
  "on-device",
  "self-hosted",
] as const satisfies readonly TrajectoryLocality[]);

/** Perceive / reason / act — cognitive loop stages (chunkable for long turns). */
export const TRAJECTORY_STAGE_NAMES = Object.freeze([
  "perceive",
  "reason",
  "act",
] as const);

export type TrajectoryStageName = (typeof TRAJECTORY_STAGE_NAMES)[number];

export const TRAJECTORY_STAGE_STATUSES = Object.freeze([
  "ok",
  "aborted",
  "error",
  "skipped",
] as const);

export type TrajectoryStageStatus = (typeof TRAJECTORY_STAGE_STATUSES)[number];

export const TRAJECTORY_TOOL_CALL_STATUSES = Object.freeze([
  "ok",
  "error",
  "aborted",
  "denied",
] as const);

export type TrajectoryToolCallStatus =
  (typeof TRAJECTORY_TOOL_CALL_STATUSES)[number];

export const TRAJECTORY_OUTCOME_STATUSES = Object.freeze([
  "completed",
  "aborted",
  "error",
  "partial",
] as const);

export type TrajectoryOutcomeStatus =
  (typeof TRAJECTORY_OUTCOME_STATUSES)[number];

/**
 * Keys that must never appear on a trajectory record or its JSON Schema.
 * Privacy assertion tests lock this set.
 */
export const TRAJECTORY_FORBIDDEN_CONTENT_KEYS = Object.freeze([
  "keystrokes",
  "rawKeystrokes",
  "utterance",
  "prompt",
  "completion",
  "reply",
  "arguments",
  "toolArgs",
  "rawArgs",
  "inputText",
  "responseText",
] as const);

/** Stage row for capture format v1 (distinct from learning TurnTrajectoryRecord.stages). */
export type TrajectoryFormatStageRecord = {
  stage: TrajectoryStageName;
  status: TrajectoryStageStatus;
  /** Chunk index for long turns — stages may be split without holding the turn lock. */
  chunkIndex?: number | undefined;
  opCode?: string | undefined;
  startedAt?: HLCTimestamp | undefined;
  endedAt?: HLCTimestamp | undefined;
};

/**
 * Tool call metadata. Args and results are hashes only in v1 —
 * regulated argument bodies never persist unless a later consent-gated
 * extension explicitly widens the format (not this slice).
 */
export type TrajectoryToolCallRecord = {
  callId: string;
  toolName: string;
  argsHash: string;
  /** Byte length of args before hashing — never the args themselves. */
  argsByteLength?: number | undefined;
  status: TrajectoryToolCallStatus;
  resultHash?: string | undefined;
  resultByteLength?: number | undefined;
};

export type TrajectoryOutcomes = {
  status: TrajectoryOutcomeStatus;
  terminalStage?: TrajectoryStageName | undefined;
};

/**
 * Turn trajectory v1 wire record.
 * Every exportable record carries {@link TRAJECTORY_FORMAT_VERSION}.
 */
export type TurnTrajectoryV1 = {
  trajectoryFormatVersion: typeof TRAJECTORY_FORMAT_VERSION;
  turnId: string;
  subjectId: string;
  deviceId: string;
  sessionId?: string | undefined;
  capturedAt: HLCTimestamp;
  locality: TrajectoryLocality;
  /** Required for export — references an active opt-in consent ledger entry. */
  consentRecordId: string;
  stages: TrajectoryFormatStageRecord[];
  toolCalls: TrajectoryToolCallRecord[];
  outcomes: TrajectoryOutcomes;
  modelId: string;
  promptHash: string;
  responseHash: string;
  promptByteLength?: number | undefined;
  responseByteLength?: number | undefined;
};

export const trajectoryStageRecordSchema = z
  .object({
    stage: z.enum(TRAJECTORY_STAGE_NAMES),
    status: z.enum(TRAJECTORY_STAGE_STATUSES),
    chunkIndex: z.number().int().nonnegative().max(TRAJECTORY_STAGE_LIMIT).optional(),
    opCode: z.string().min(1).max(64).optional(),
    startedAt: hlcSchema.optional(),
    endedAt: hlcSchema.optional(),
  })
  .strict() satisfies z.ZodType<TrajectoryFormatStageRecord>;

export const trajectoryToolCallRecordSchema = z
  .object({
    callId: z.string().min(1).max(TRAJECTORY_ID_LIMIT),
    toolName: z.string().min(1).max(TRAJECTORY_ID_LIMIT),
    argsHash: z.string().min(1).max(TRAJECTORY_HASH_LIMIT),
    argsByteLength: z.number().int().nonnegative().optional(),
    status: z.enum(TRAJECTORY_TOOL_CALL_STATUSES),
    resultHash: z.string().min(1).max(TRAJECTORY_HASH_LIMIT).optional(),
    resultByteLength: z.number().int().nonnegative().optional(),
  })
  .strict()
  .superRefine((rec, ctx) => {
    for (const key of TRAJECTORY_FORBIDDEN_CONTENT_KEYS) {
      if (Object.prototype.hasOwnProperty.call(rec, key)) {
        ctx.addIssue({
          code: "custom",
          path: [key],
          message: `raw content key '${key}' forbidden on tool call — hashes only`,
        });
      }
    }
  }) satisfies z.ZodType<TrajectoryToolCallRecord>;

export const trajectoryOutcomesSchema = z
  .object({
    status: z.enum(TRAJECTORY_OUTCOME_STATUSES),
    terminalStage: z.enum(TRAJECTORY_STAGE_NAMES).optional(),
  })
  .strict() satisfies z.ZodType<TrajectoryOutcomes>;

export const turnTrajectoryV1Schema = z
  .object({
    trajectoryFormatVersion: z.literal(TRAJECTORY_FORMAT_VERSION),
    turnId: z.string().min(1).max(TRAJECTORY_ID_LIMIT),
    subjectId: z.string().min(1).max(TRAJECTORY_ID_LIMIT),
    deviceId: z.string().min(1).max(TRAJECTORY_ID_LIMIT),
    sessionId: z.string().min(1).max(TRAJECTORY_ID_LIMIT).optional(),
    capturedAt: hlcSchema,
    locality: z.enum(TRAJECTORY_LOCALITIES),
    consentRecordId: z.string().min(1).max(TRAJECTORY_ID_LIMIT),
    stages: z.array(trajectoryStageRecordSchema).max(TRAJECTORY_STAGE_LIMIT),
    toolCalls: z
      .array(trajectoryToolCallRecordSchema)
      .max(TRAJECTORY_TOOL_CALL_LIMIT),
    outcomes: trajectoryOutcomesSchema,
    modelId: z.string().min(1).max(TRAJECTORY_MODEL_ID_LIMIT),
    promptHash: z.string().min(1).max(TRAJECTORY_HASH_LIMIT),
    responseHash: z.string().min(1).max(TRAJECTORY_HASH_LIMIT),
    promptByteLength: z.number().int().nonnegative().optional(),
    responseByteLength: z.number().int().nonnegative().optional(),
  })
  .strict()
  .superRefine((rec, ctx) => {
    for (const key of TRAJECTORY_FORBIDDEN_CONTENT_KEYS) {
      if (Object.prototype.hasOwnProperty.call(rec, key)) {
        ctx.addIssue({
          code: "custom",
          path: [key],
          message: `raw content key '${key}' forbidden — hashes and structured metadata only`,
        });
      }
    }
  }) satisfies z.ZodType<TurnTrajectoryV1>;

export type TrajectoryFailureClass =
  | "validation"
  | "keystroke_forbidden"
  | "consent_missing"
  | "consent_denied"
  | "cross_subject"
  | "stage_limit"
  | "tool_call_limit"
  | "write_failed";

export type ParseTurnTrajectoryV1Accepted = {
  ok: true;
  record: TurnTrajectoryV1;
  subjectId: string;
  deviceId: string;
};

export type ParseTurnTrajectoryV1Rejected = {
  ok: false;
  failureClass: TrajectoryFailureClass;
  subjectId: string | null;
  detail: string;
  issuePath?: string;
};

export type ParseTurnTrajectoryV1Result =
  | ParseTurnTrajectoryV1Accepted
  | ParseTurnTrajectoryV1Rejected;

function hasForbiddenContentKey(
  value: unknown,
): { found: true; key: string } | { found: false } {
  if (value === null || typeof value !== "object") return { found: false };
  const record = value as Record<string, unknown>;
  for (const key of TRAJECTORY_FORBIDDEN_CONTENT_KEYS) {
    if (key in record) return { found: true, key };
  }
  if (Array.isArray(record.toolCalls)) {
    for (const call of record.toolCalls) {
      if (call !== null && typeof call === "object") {
        for (const key of TRAJECTORY_FORBIDDEN_CONTENT_KEYS) {
          if (key in (call as object)) return { found: true, key };
        }
      }
    }
  }
  if (Array.isArray(record.stages)) {
    for (const stage of record.stages) {
      if (stage !== null && typeof stage === "object") {
        for (const key of TRAJECTORY_FORBIDDEN_CONTENT_KEYS) {
          if (key in (stage as object)) return { found: true, key };
        }
      }
    }
  }
  return { found: false };
}

/**
 * Privacy assertion: schema property bags must never include forbidden content keys.
 * Used by unit tests and export gates.
 */
export function assertTrajectorySchemaPrivacy(
  schemaDocument: Record<string, unknown>,
):
  | { ok: true }
  | { ok: false; failureClass: "keystroke_forbidden"; detail: string; key: string } {
  const walk = (
    node: unknown,
    path: string,
  ): { ok: false; key: string; path: string } | null => {
    if (node === null || typeof node !== "object") return null;
    if (Array.isArray(node)) {
      for (let i = 0; i < node.length; i++) {
        const hit = walk(node[i], `${path}[${i}]`);
        if (hit) return hit;
      }
      return null;
    }
    const obj = node as Record<string, unknown>;
    if ("properties" in obj && obj.properties && typeof obj.properties === "object") {
      for (const key of Object.keys(obj.properties as object)) {
        if (
          (TRAJECTORY_FORBIDDEN_CONTENT_KEYS as readonly string[]).includes(key)
        ) {
          return { ok: false, key, path: `${path}.properties` };
        }
        const hit = walk(
          (obj.properties as Record<string, unknown>)[key],
          `${path}.properties.${key}`,
        );
        if (hit) return hit;
      }
    }
    for (const [k, v] of Object.entries(obj)) {
      if (k === "properties") continue;
      const hit = walk(v, `${path}.${k}`);
      if (hit) return hit;
    }
    return null;
  };

  const hit = walk(schemaDocument, "$");
  if (hit) {
    return {
      ok: false,
      failureClass: "keystroke_forbidden",
      detail: `forbidden content key '${hit.key}' at ${hit.path}`,
      key: hit.key,
    };
  }
  return { ok: true };
}

export function parseTurnTrajectoryV1(
  input: unknown,
): ParseTurnTrajectoryV1Result {
  const forbidden = hasForbiddenContentKey(input);
  if (forbidden.found) {
    return {
      ok: false,
      failureClass: "keystroke_forbidden",
      subjectId:
        input !== null &&
        typeof input === "object" &&
        typeof (input as { subjectId?: unknown }).subjectId === "string"
          ? (input as { subjectId: string }).subjectId
          : null,
      detail: `forbidden content key '${forbidden.key}'`,
      issuePath: forbidden.key,
    };
  }

  const parsed = turnTrajectoryV1Schema.safeParse(input);
  if (!parsed.success) {
    const first = parsed.error.issues[0];
    const issuePath = first
      ? first.path.map(String).join(".") || "(root)"
      : "(root)";
    const missingConsent =
      issuePath === "consentRecordId" ||
      (first?.message ?? "").includes("consentRecordId");
    const stageLimit =
      issuePath.startsWith("stages") &&
      (first?.message ?? "").toLowerCase().includes("array");
    const toolLimit =
      issuePath.startsWith("toolCalls") &&
      (first?.message ?? "").toLowerCase().includes("array");
    return {
      ok: false,
      failureClass: missingConsent
        ? "consent_missing"
        : stageLimit
          ? "stage_limit"
          : toolLimit
            ? "tool_call_limit"
            : "validation",
      subjectId:
        input !== null &&
        typeof input === "object" &&
        typeof (input as { subjectId?: unknown }).subjectId === "string"
          ? (input as { subjectId: string }).subjectId
          : null,
      detail: first?.message ?? "turn trajectory v1 validation failed",
      issuePath,
    };
  }

  return {
    ok: true,
    record: parsed.data,
    subjectId: parsed.data.subjectId,
    deviceId: parsed.data.deviceId,
  };
}

/**
 * Export gate — requires consentRecordId and subject isolation check against ledger.
 */
export function assertTurnTrajectoryExportConsent(
  record: TurnTrajectoryV1,
  resolveConsent: (
    consentRecordId: string,
  ) =>
    | {
        consentRecordId: string;
        subjectId: string;
        optedIn: boolean;
        active: boolean;
      }
    | null
    | undefined,
):
  | { ok: true; subjectId: string; consentRecordId: string }
  | {
      ok: false;
      failureClass: TrajectoryFailureClass;
      subjectId: string | null;
      detail: string;
    } {
  if (!record.subjectId) {
    return {
      ok: false,
      failureClass: "validation",
      subjectId: null,
      detail: "subjectId required",
    };
  }
  if (!record.consentRecordId) {
    return {
      ok: false,
      failureClass: "consent_missing",
      subjectId: record.subjectId,
      detail: "consentRecordId required for trajectory export",
    };
  }

  let resolved:
    | {
        consentRecordId: string;
        subjectId: string;
        optedIn: boolean;
        active: boolean;
      }
    | null
    | undefined;
  try {
    resolved = resolveConsent(record.consentRecordId);
  } catch (err: unknown) {
    return {
      ok: false,
      failureClass: "validation",
      subjectId: record.subjectId,
      detail:
        err instanceof Error
          ? `consent resolve failed: ${err.name}`
          : "consent resolve failed",
    };
  }

  if (!resolved) {
    return {
      ok: false,
      failureClass: "consent_missing",
      subjectId: record.subjectId,
      detail: "consent record not found",
    };
  }
  if (resolved.subjectId !== record.subjectId) {
    return {
      ok: false,
      failureClass: "cross_subject",
      subjectId: record.subjectId,
      detail: "consent subjectId must match trajectory subjectId",
    };
  }
  if (!resolved.active || !resolved.optedIn) {
    return {
      ok: false,
      failureClass: "consent_denied",
      subjectId: record.subjectId,
      detail: "trajectory export requires active opted-in consent",
    };
  }

  return {
    ok: true,
    subjectId: record.subjectId,
    consentRecordId: record.consentRecordId,
  };
}

export type TrajectoryWriteTelemetryEvent = {
  event: "telemetry.trajectory.write";
  outcome: "queued" | "ok" | "rejected";
  subjectId: string;
  deviceId?: string;
  failureClass?: string;
};

/**
 * Schedule an async trajectory persist — returns immediately so the turn
 * hot-path is never blocked waiting on durable I/O (chunked stages included).
 */
export function enqueueTurnTrajectoryWrite(
  record: TurnTrajectoryV1,
  writer: (record: TurnTrajectoryV1) => void | Promise<void>,
  options: {
    onTelemetry?: (event: TrajectoryWriteTelemetryEvent) => void;
  } = {},
): { queued: true; subjectId: string } {
  const subjectId = record.subjectId;
  options.onTelemetry?.({
    event: "telemetry.trajectory.write",
    outcome: "queued",
    subjectId,
    deviceId: record.deviceId,
  });

  void Promise.resolve()
    .then(() => writer(record))
    .then(() => {
      options.onTelemetry?.({
        event: "telemetry.trajectory.write",
        outcome: "ok",
        subjectId,
        deviceId: record.deviceId,
      });
    })
    .catch((err: unknown) => {
      options.onTelemetry?.({
        event: "telemetry.trajectory.write",
        outcome: "rejected",
        subjectId,
        deviceId: record.deviceId,
        failureClass:
          err instanceof Error ? err.name.slice(0, 64) : "write_failed",
      });
    });

  return { queued: true, subjectId };
}

/**
 * Canonical JSON Schema document for TurnTrajectoryV1 (draft-07).
 * Matches the committed exporter output (plus privacy-gate metadata helpers).
 */
export function toTurnTrajectoryJsonSchema(
  protocolVersion: string,
): Record<string, unknown> {
  const raw = z.toJSONSchema(turnTrajectoryV1Schema, {
    target: "draft-07",
    io: "input",
    reused: "inline",
    cycles: "ref",
  });
  const MAX_SAFE = 9007199254740991;
  const stripZodNoise = (node: unknown): unknown => {
    if (Array.isArray(node)) return node.map(stripZodNoise);
    if (!node || typeof node !== "object") return node;
    const out: Record<string, unknown> = {};
    for (const [k, v] of Object.entries(node as Record<string, unknown>)) {
      if (k === "maximum" && v === MAX_SAFE) continue;
      if (k === "propertyNames") continue;
      if (k === "additionalProperties" && v === false) continue;
      out[k] = stripZodNoise(v);
    }
    return out;
  };
  return {
    ...(stripZodNoise(raw) as Record<string, unknown>),
    title: "TurnTrajectoryV1",
    "x-protocol-version": protocolVersion,
    "x-trajectory-format-version": TRAJECTORY_FORMAT_VERSION,
  };
}
