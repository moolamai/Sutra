import {
  hlcSchema,
  type HLCTimestamp,
  type TrajectoryLocality,
} from "./contract.js";

/** Format version — bump MINOR for additive field additions only. */
export const TRAJECTORY_FORMAT_VERSION = "trajectory.v1" as const;

