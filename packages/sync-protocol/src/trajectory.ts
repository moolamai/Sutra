/**
 * Turn trajectory format v1 — metadata-grade capture wire schema.
 *
 * Records stages, tool calls, outcomes, content hashes, and model id.
 * Raw keystrokes, prompts, and tool argument bodies never enter the format —
 * only hashes and length metadata. Export requires an active consentRecordId.
 */

