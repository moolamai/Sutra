/**
 * TurnTrajectoryRecord — B9 metadata-grade trajectory + C0 training fields.
 *
 * Zod parse boundary shared by corpus factories, gyms, and critics.
 * Structured turn records only — never raw keystrokes or utterance bodies.
 *
 * Evolution (additive only):
 * - Existing B9 records without training fields MUST parse unchanged.
 * - `routerReplayMap` is optional; dense on-device SLMs may omit it.
 *   Sparse / router-using hosts may include it for offline replay — parsers
 *   MUST accept both present and absent forms (forward-compat for dense SLMs).
 *
 * Consent: every exportable record carries an explicit opt-in consent object.
 * Storage stays sovereign-side; export requires consent.optedIn === true.
 */

import { z } from "zod";

/** Trajectory schema id — bump MINOR for additive field additions only. */
export const TRAJECTORY_SCHEMA_VERSION = "trajectory.v1" as const;

/**
 * Repo-relative path to the committed JSON Schema export (W3 freeze).
 * Regenerated by `pnpm --filter @moolam/learning schemas:export`.
 */
export const TRAJECTORY_COMMITTED_SCHEMA_RELPATH =
  "schemas/trajectory/v1.json" as const;

/**
 * Package-relative golden fixture root (B9 backward-compat + C0 extended).
 * Covered by round-trip / violation suite in `@moolam/learning` tests.
 */
export const TRAJECTORY_GOLDEN_FIXTURES_RELPATH =
  "fixtures/trajectory" as const;

/**
 * C0 training field classes — optional on the wire; when present, nested
 * required members and enum/hash constraints apply. Violation fixtures cover
 * one reject path per class (and B9 keystroke forbid).
 */
export const TRAJECTORY_TRAINING_FIELD_CLASSES = Object.freeze([
  "policyCheckpointHash",
  "rolloutSeed",
  "precisionFormat",
  "executionState",
  "routerReplayMap",
] as const);

export type TrajectoryTrainingFieldClass =
  (typeof TRAJECTORY_TRAINING_FIELD_CLASSES)[number];

/** Soft caps (NFR — bounded maps / stage lists). */
export const TRAJECTORY_STAGE_LIMIT = 32;
export const TRAJECTORY_ROUTER_REPLAY_MAP_LIMIT = 64;
export const TRAJECTORY_HASH_LIMIT = 128;
export const TRAJECTORY_ID_LIMIT = 128;

/**
 * Quantization / precision tag for the adapter/base used during rollout.
 * Opaque training metadata — never learner content.
 */
export const PRECISION_FORMATS = Object.freeze([
  "fp32",
  "fp16",
  "bf16",
  "int8",
  "int4",
  "nf4",
] as const);

export type PrecisionFormat = (typeof PRECISION_FORMATS)[number];

export const CONSENT_CLASSES = Object.freeze([
  "research",
  "product-improve",
  "personal",
] as const);

export type TrajectoryConsentClass = (typeof CONSENT_CLASSES)[number];

export const TRAJECTORY_LOCALITIES = Object.freeze([
  "on-device",
  "self-hosted",
] as const);

export type TrajectoryLocality = (typeof TRAJECTORY_LOCALITIES)[number];

/**
 * Per-turn execution state for training.
 * On stream abort / partial turns: record the last attempted command and
 * terminal status — do not omit the object silently when training fields apply.
 */
export type TrajectoryExecutionState = {
  /** Opaque opcode / tool name last attempted — never keystroke streams. */
  commandExecuted: string;
  /** Terminal status (HTTP-like int or opaque code string). */
  statusCode: number | string;
};

/**
 * Forward-compat router replay map for offline sparse-router trajectories.
 * Dense SLMs omit this field; absence is valid and must not fail parse.
 */
export type RouterReplayMap = Readonly<Record<string, string>>;

export type TrajectoryConsent = {
  optedIn: boolean;
  consentClass: TrajectoryConsentClass;
  /** Opaque capture time (ISO or HLC) — never utterance text. */
  recordedAt: string;
};

export type TrajectoryStageRecord = {
  stage: string;
  opCode?: string | undefined;
  /** Structured terminal — not model prose. */
  status?: "ok" | "aborted" | "error" | "skipped" | undefined;
};

/**
 * Full turn trajectory record (B9 base + optional C0 training extensions).
 * Optional keys use `| undefined` for exactOptionalPropertyTypes + Zod output.
 */
export type TurnTrajectoryRecord = {
  schemaVersion: typeof TRAJECTORY_SCHEMA_VERSION;
  subjectId: string;
  sessionId: string;
  turnId: string;
  deviceId?: string | undefined;
  capturedAt: string;
  locality: TrajectoryLocality;
  consent: TrajectoryConsent;
  stages: TrajectoryStageRecord[];
  /** Opaque tool-call ids only — never argument bodies. */
  toolCallIds?: string[] | undefined;
  /**
   * Exact adapter/base checkpoint hash used during rollout (IMPALA lineage).
   * Must bind a concrete checkpoint — never the floating token "latest".
   */
  policyCheckpointHash?: string | undefined;
  /** Gym / fleet rollout seed for IMPALA lineage (opaque int, not utterance text). */
  rolloutSeed?: number | undefined;
  /** Quantization tag for the SLM / adapter used at capture time. */
  precisionFormat?: PrecisionFormat | undefined;
  /** Last attempted command + terminal status for this turn. */
  executionState?: TrajectoryExecutionState | undefined;
  /**
   * Optional router replay map. Dense on-device SLMs MAY omit this field;
   * sparse hosts MAY include it. Parsers must accept both (forward-compat).
   */
  routerReplayMap?: RouterReplayMap | undefined;
};

export type TrajectoryParseFailureClass =
  | "schema_violation"
  | "missing_subject"
  | "consent_denied"
  | "floating_checkpoint"
  | "keystroke_forbidden"
  | "section_limit";

const FORBIDDEN_CONTENT_KEYS = Object.freeze([
  "keystrokes",
  "rawKeystrokes",
  "utterance",
  "prompt",
  "completion",
  "reply",
  "arguments",
  "toolArgs",
]);

export const trajectoryConsentSchema = z
  .object({
    optedIn: z.boolean(),
    consentClass: z.enum(CONSENT_CLASSES),
    recordedAt: z.string().min(1).max(TRAJECTORY_HASH_LIMIT),
  })
  .strict();

export const trajectoryExecutionStateSchema = z
  .object({
    commandExecuted: z.string().min(1).max(256),
    statusCode: z.union([
      z.number().int().min(0).max(9999),
      z.string().min(1).max(64),
    ]),
  })
  .strict();

export const trajectoryStageRecordSchema = z
  .object({
    stage: z.string().min(1).max(64),
    opCode: z.string().min(1).max(64).optional(),
    status: z.enum(["ok", "aborted", "error", "skipped"]).optional(),
  })
  .strict();

export const routerReplayMapSchema = z
  .record(z.string().min(1).max(TRAJECTORY_ID_LIMIT), z.string().min(1).max(256))
  .refine(
    (m) => Object.keys(m).length <= TRAJECTORY_ROUTER_REPLAY_MAP_LIMIT,
    `routerReplayMap exceeds ${TRAJECTORY_ROUTER_REPLAY_MAP_LIMIT} entries`,
  );

const policyCheckpointHashSchema = z
  .string()
  .min(8)
  .max(TRAJECTORY_HASH_LIMIT)
  .regex(/^[A-Za-z0-9:_+-]+$/, "policyCheckpointHash must be opaque hash id")
  .refine(
    (h) => h.toLowerCase() !== "latest",
    "policyCheckpointHash must bind an exact checkpoint — not floating 'latest'",
  );

/**
 * Canonical Zod schema for TurnTrajectoryRecord.
 * Additive evolution: training fields optional; routerReplayMap optional.
 */
export const turnTrajectoryRecordSchema = z
  .object({
    schemaVersion: z.literal(TRAJECTORY_SCHEMA_VERSION),
    subjectId: z.string().min(1).max(TRAJECTORY_ID_LIMIT),
    sessionId: z.string().min(1).max(TRAJECTORY_ID_LIMIT),
    turnId: z.string().min(1).max(TRAJECTORY_ID_LIMIT),
    deviceId: z.string().min(1).max(TRAJECTORY_ID_LIMIT).optional(),
    capturedAt: z.string().min(1).max(TRAJECTORY_HASH_LIMIT),
    locality: z.enum(TRAJECTORY_LOCALITIES),
    consent: trajectoryConsentSchema,
    stages: z
      .array(trajectoryStageRecordSchema)
      .min(1)
      .max(TRAJECTORY_STAGE_LIMIT),
    toolCallIds: z
      .array(z.string().min(1).max(TRAJECTORY_ID_LIMIT))
      .max(TRAJECTORY_STAGE_LIMIT)
      .optional(),
    policyCheckpointHash: policyCheckpointHashSchema.optional(),
    rolloutSeed: z.number().int().min(0).max(0xffff_ffff).optional(),
    precisionFormat: z.enum(PRECISION_FORMATS).optional(),
    executionState: trajectoryExecutionStateSchema.optional(),
    /**
     * Forward-compat: dense SLMs omit; parsers must not require this key.
     */
    routerReplayMap: routerReplayMapSchema.optional(),
  })
  .strict()
  .superRefine((rec, ctx) => {
    for (const key of FORBIDDEN_CONTENT_KEYS) {
      if (Object.prototype.hasOwnProperty.call(rec, key)) {
        ctx.addIssue({
          code: "custom",
          path: [key],
          message: `raw content key '${key}' forbidden — structured metadata only`,
        });
      }
    }
  });

export type ParseTurnTrajectoryAccepted = {
  ok: true;
  record: TurnTrajectoryRecord;
  subjectId: string;
  deviceId?: string;
};

export type ParseTurnTrajectoryRejected = {
  ok: false;
  failureClass: TrajectoryParseFailureClass;
  subjectId: string | null;
  deviceId?: string;
  detail: string;
  issuePath?: string;
};

export type ParseTurnTrajectoryResult =
  | ParseTurnTrajectoryAccepted
  | ParseTurnTrajectoryRejected;

/**
 * Parse and validate one TurnTrajectoryRecord at the learning boundary.
 */
export function parseTurnTrajectoryRecord(
  input: unknown,
): ParseTurnTrajectoryResult {
  if (
    input &&
    typeof input === "object" &&
    !Array.isArray(input)
  ) {
    const record = input as Record<string, unknown>;
    for (const key of FORBIDDEN_CONTENT_KEYS) {
      if (key in record) {
        return {
          ok: false,
          failureClass: "keystroke_forbidden",
          subjectId:
            typeof record.subjectId === "string" ? record.subjectId : null,
          detail: `forbidden content key '${key}'`,
          issuePath: key,
        };
      }
    }
  }

  const parsed = turnTrajectoryRecordSchema.safeParse(input);
  if (!parsed.success) {
    const first = parsed.error.issues[0];
    const issuePath = first
      ? first.path.map(String).join(".") || "(root)"
      : "(root)";
    const message = first?.message ?? "TurnTrajectoryRecord schema_violation";
    const subjectGuess =
      input &&
      typeof input === "object" &&
      typeof (input as { subjectId?: unknown }).subjectId === "string"
        ? (input as { subjectId: string }).subjectId
        : null;

    let failureClass: TrajectoryParseFailureClass = "schema_violation";
    if (issuePath === "subjectId" || message.includes("subjectId")) {
      failureClass = "missing_subject";
    } else if (
      message.includes("latest") ||
      issuePath.includes("policyCheckpointHash")
    ) {
      failureClass = "floating_checkpoint";
    } else if (message.includes("exceeds") || message.includes("limit")) {
      failureClass = "section_limit";
    }

    return {
      ok: false,
      failureClass,
      subjectId: subjectGuess && subjectGuess.length > 0 ? subjectGuess : null,
      detail: message,
      issuePath,
    };
  }

  const record = parsed.data;
  return {
    ok: true,
    record,
    subjectId: record.subjectId,
    ...(record.deviceId !== undefined ? { deviceId: record.deviceId } : {}),
  };
}

/**
 * Consent gate for export / leaving the sovereign boundary.
 * Declined opt-in is a hard deny — not silent pass-through.
 */
export function assertTrajectoryExportConsent(record: TurnTrajectoryRecord):
  | { ok: true; subjectId: string }
  | {
      ok: false;
      failureClass: "consent_denied" | "missing_subject";
      subjectId: string | null;
      detail: string;
    } {
  if (!record.subjectId) {
    return {
      ok: false,
      failureClass: "missing_subject",
      subjectId: null,
      detail: "subjectId required",
    };
  }
  if (record.consent.optedIn !== true) {
    return {
      ok: false,
      failureClass: "consent_denied",
      subjectId: record.subjectId,
      detail: "export requires consent.optedIn === true",
    };
  }
  return { ok: true, subjectId: record.subjectId };
}

/**
 * Schedule an async trajectory persist — returns immediately so the turn
 * hot-path is never blocked waiting on durable I/O.
 */
export function enqueueTrajectoryWrite(
  record: TurnTrajectoryRecord,
  writer: (record: TurnTrajectoryRecord) => void | Promise<void>,
  options: {
    onTelemetry?: (event: {
      event: "learning.trajectory.write";
      outcome: "queued" | "ok" | "rejected";
      subjectId: string;
      deviceId?: string;
      failureClass?: string;
    }) => void;
  } = {},
): { queued: true; subjectId: string } {
  const subjectId = record.subjectId;
  options.onTelemetry?.({
    event: "learning.trajectory.write",
    outcome: "queued",
    subjectId,
    ...(record.deviceId !== undefined ? { deviceId: record.deviceId } : {}),
  });

  void Promise.resolve()
    .then(() => writer(record))
    .then(() => {
      options.onTelemetry?.({
        event: "learning.trajectory.write",
        outcome: "ok",
        subjectId,
        ...(record.deviceId !== undefined ? { deviceId: record.deviceId } : {}),
      });
    })
    .catch((err: unknown) => {
      options.onTelemetry?.({
        event: "learning.trajectory.write",
        outcome: "rejected",
        subjectId,
        ...(record.deviceId !== undefined ? { deviceId: record.deviceId } : {}),
        failureClass:
          err instanceof Error ? err.name.slice(0, 64) : "write_failed",
      });
    });

  return { queued: true, subjectId };
}

