/**
 * Stable key-sorted JSON for trajectory wire / JSONL identity.
 * Strip `undefined` so re-parse matches B9 (no phantom training keys).
 */
export function toCanonicalTrajectoryJson(record: TurnTrajectoryRecord): string {
  const stripUndefined = (value: unknown): unknown => {
    if (Array.isArray(value)) return value.map(stripUndefined);
    if (value !== null && typeof value === "object") {
      const out: Record<string, unknown> = {};
      for (const key of Object.keys(value as object).sort()) {
        const v = (value as Record<string, unknown>)[key];
        if (v === undefined) continue;
        out[key] = stripUndefined(v);
      }
      return out;
    }
    return value;
  };
  return `${JSON.stringify(stripUndefined(record), null, 2)}\n`;
}
